long int	ft_add(long int a, long int b)
{
	return (a + b);
}

long int	ft_substract(long int a, long int b)
{
	return (a - b);
}

long int	ft_divide(long int a, long int b)
{
	return (a / b);
}

long int	ft_multiply(long int a, long int b)
{
	return (a * b);
}

long int	ft_modulo(long int a, long int b)
{
	return (a % b);
}
